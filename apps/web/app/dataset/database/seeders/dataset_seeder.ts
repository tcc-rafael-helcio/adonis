import { BaseSeeder } from '@adonisjs/lucid/seeders'

import app from '@adonisjs/core/services/app'
import Database from '@adonisjs/lucid/services/db'

import { mkdir, writeFile } from 'node:fs/promises'
import { join } from 'node:path'

import User from '#users/models/user'
import Dataset from '#app/dataset/models/dataset'
import DatasetVersion from '#app/dataset/models/dataset_version'

export default class DatasetSeeder extends BaseSeeder {
  async run() {
    const datasetName = 'Seeder Dataset'
    const versionName = 'V1'
    const csvFileName = 'seed.csv'

    const rootPath = app.makePath('storage/datasets')
    const datasetPath = join(rootPath, datasetName)
    const versionPath = join(datasetPath, versionName)
    const readmePath = join(versionPath, 'README.md')
    const csvPath = join(versionPath, csvFileName)

    await mkdir(versionPath, { recursive: true })

    await writeFile(
      readmePath,
      ['# Seeder Dataset', '', 'Version: V1', '', 'Generated by dataset seeder.', ''].join('\n'),
      'utf8'
    )

    await writeFile(csvPath, ['id,name', '1,Sample row'].join('\n') + '\n', 'utf8')

    const owner = await User.query().orderBy('id', 'asc').first()

    if (!owner) {
      throw new Error('DatasetSeeder requires at least one user in the database.')
    }

    await Database.transaction(async (trx) => {
      let dataset = await Dataset.query({ client: trx }).where('path', datasetPath).first()

      if (!dataset) {
        dataset = await Dataset.create(
          {
            name: datasetName,
            path: datasetPath,
            isPublic: false,
            userId: owner.id,
          },
          { client: trx }
        )
      } else {
        dataset.merge({
          name: datasetName,
          userId: owner.id,
          isPublic: false,
        })
        await dataset.save()
      }

      let version = await DatasetVersion.query({ client: trx })
        .where('dataset_id', dataset.id)
        .where('name', versionName)
        .first()

      if (!version) {
        await DatasetVersion.create(
          {
            datasetId: dataset.id,
            name: versionName,
            path: csvPath,
          },
          { client: trx }
        )
      } else {
        version.merge({ path: csvPath })
        await version.save()
      }
    })
  }
}
